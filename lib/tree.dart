class Tree {
    final int treeId;
    final String commonName;
    final String cultivarName;
    final DateTime datePlanted;
    final double diameter;
    final String genusName;
    final double latitude;
    final double longitude;
    final String speciesName;

    Tree({this.treeId, this.commonName, this.cultivarName, this.datePlanted, this.diameter,  this.genusName, this.latitude, this.longitude, this.speciesName}); 
}